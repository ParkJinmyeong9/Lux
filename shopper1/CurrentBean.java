package shopper;

public class CurrentBean {
	private int currentID;
	private int cProductID;
	private int waybillNum;
	private int cUserID;
	
	public int getCUserID() {
		return cUserID;
	}
	public void setCUserID(int cUserID) {
		this.cUserID = cUserID;
	}
	public int getCurrentID() {
		return currentID;
	}
	public void setCurrentID(int currentID) {
		this.currentID = currentID;
	}
	public int getCProductID() {
		return cProductID;
	}
	public void setCProductID(int cProductID) {
		this.cProductID = cProductID;
	}
	public int getWaybillNum() {
		return waybillNum;
	}
	public void setWaybillNum(int waybillNum) {
		this.waybillNum = waybillNum;
	}
	
	
}
